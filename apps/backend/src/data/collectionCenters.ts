const VERIFIED_AT = '2026-06-04';

function regionalCenter(
  center: Omit<CollectionCenter, 'openingHours'> & { openingHours?: string },
): CollectionCenter {
  return {
    region: 'Campania',
    kind: 'Servizio trasfusionale',
    sourceName: REGION_SOURCE,
    sourceUrl: REGION_SOURCE_URL,
    lastVerifiedAt: VERIFIED_AT,
    bookingMode: 'Simulata',
    openingHours: 'Contattare la struttura',
    ...center,
  };
}

